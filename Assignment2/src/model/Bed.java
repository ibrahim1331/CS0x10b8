package model;

public class Bed {
	private Integer bedId;
	private String type;
	private String size;
	
	public Integer getBedId() {
		return bedId;
	}
	
	public void setBedId(Integer bedId) {
		this.bedId = bedId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}
}
